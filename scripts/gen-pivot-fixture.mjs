/**
 * Emit a real .xlsx containing a NATIVE in-app pivot (xl/pivotTables +
 * xl/pivotCaches generated by pivot-export.ts and injected via the
 * passthrough machinery). The whole point of this file is the validation
 * the native-export work needs but CI can't do: open it in Excel and confirm
 * it loads WITHOUT a "we found a problem / repair" prompt, and shows a real
 * PivotTable (Region rows, Sum of Sales) at E1.
 *
 *   node --import tsx scripts/gen-pivot-fixture.mjs
 *   open /tmp/casual-pivot-native.xlsx     # then check Excel
 */
import { writeFileSync } from 'node:fs';
import ExcelJS from 'exceljs';
import JSZip from 'jszip';
import { generateNativePivot } from '../packages/sdk/src/xlsx/pivot-export.ts';
import { applyPivotsToZip } from '../packages/sdk/src/xlsx/pivot-passthrough.ts';

const records = [
  ['North', 'Q1', 100],
  ['South', 'Q1', 80],
  ['North', 'Q2', 120],
  ['South', 'Q2', 95],
];

const wb = new ExcelJS.Workbook();
const ws = wb.addWorksheet('Sheet1');
ws.addRow(['Region', 'Quarter', 'Sales']);
for (const r of records) ws.addRow(r);
// Materialised pivot output (what our compute writes) at E1:F4.
ws.getCell('E1').value = 'Region';
ws.getCell('F1').value = 'Sum of Sales';
ws.getCell('E2').value = 'North';
ws.getCell('F2').value = 220;
ws.getCell('E3').value = 'South';
ws.getCell('F3').value = 175;
ws.getCell('E4').value = 'Grand Total';
ws.getCell('F4').value = 395;

const base = await wb.xlsx.writeBuffer();
const zip = await JSZip.loadAsync(base);
await applyPivotsToZip(
  zip,
  generateNativePivot({
    sheetName: 'Sheet1',
    sourceRef: 'A1:C5',
    headers: ['Region', 'Quarter', 'Sales'],
    records,
    rowField: 0,
    valueField: 2,
    valueAgg: 'sum',
    targetRef: 'E1',
  }),
);
const out = await zip.generateAsync({ type: 'nodebuffer' });
const path = '/tmp/casual-pivot-native.xlsx';
writeFileSync(path, out);
console.log(`[pivot-fixture] wrote ${path} (${out.length} bytes) — open in Excel to validate`);
